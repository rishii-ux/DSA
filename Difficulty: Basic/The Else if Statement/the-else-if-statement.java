class Solution {
	public static void utility(int number) {
		// Write the if, else if, else statements below
		if (number>100) {
			System.out.print("Big");
			System.out.println();
		}
		else if (number<10) {
			System.out.print("Small");
			System.out.println();
		}
		else {
			System.out.print("Number");
			System.out.println();
		}
	}
}
