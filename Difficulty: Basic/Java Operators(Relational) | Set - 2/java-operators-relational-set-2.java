class Solution {
    static void relationalOperators(int A, int B) {
        // code here
        if(A>B){
            System.out.print(A+" is greater than "+B);
            System.out.println();
        }
        else if(A<B){
            System.out.print(A+" is less than "+B);
            System.out.println();
        }
        else{
         System.out.print(A+" is equal to "+B);   
         System.out.println();
        }
    }
}